package petbill.hospital.model;

public class HospitalDTO {
	
	private Integer hosNo;
	private String hosNum;
	private String hosSi;
	private String hosGu;
	private String hosName;
	private String hosNewAddress;
	private String x;
	private String y;
	
	public Integer getHosNo() {
		return hosNo;
	}
	public void setHosNo(Integer hosNo) {
		this.hosNo = hosNo;
	}
	public String getHosNum() {
		return hosNum;
	}
	public void setHosNum(String hosNum) {
		this.hosNum = hosNum;
	}
	public String getHosSi() {
		return hosSi;
	}
	public void setHosSi(String hosSi) {
		this.hosSi = hosSi;
	}
	public String getHosGu() {
		return hosGu;
	}
	public void setHosGu(String hosGu) {
		this.hosGu = hosGu;
	}
	public String getHosName() {
		return hosName;
	}
	public void setHosName(String hosName) {
		this.hosName = hosName;
	}
	public String getHosNewAddress() {
		return hosNewAddress;
	}
	public void setHosNewAddress(String hosNewAddress) {
		this.hosNewAddress = hosNewAddress;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	
	
	

}
